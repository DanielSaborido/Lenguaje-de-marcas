var nota=prompt("Escribe una nota")
if (nota>=0,nota<5){
    alert("Suspenso")
}
if (nota>=5){
    alert("Aprobado")
}
if (nota==6){
    alert("Bien")
}
if (nota>=7,nota<=8){
    alert("notable")
}
if (nota>=9,nota<=10){
    alert("Sobresaliente")
}
else{
    alert("La nota introducida no es valida.")
}